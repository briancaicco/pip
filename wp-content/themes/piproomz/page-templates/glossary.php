											</p>
										</dd>
									</div>
									<div class="glossary-item col-12 col-lg-8 mb-4 bg-light p-3">
										<dt class="term h5">Closing</dt>
										<dd class="definition">
											<p>
												The process of stopping (closing) a live trade by executing a trade that is the exact opposite of the open trade.
											</p>
										</dd>
									</div>
